// ============================================================
// 決定論的な安全ガード
// AIの判定を待たず、最も重篤な表現はキーワードで確実に検知して
// エスカレーションに倒す（AIスコアとの二重チェック）
// ============================================================

// 希死念慮・自傷・重大な暴力・不正など、即時に人へつなぐべき語
// ※安全側に倒す設計。多少の過検知よりも見逃しゼロを優先する。
const CRITICAL_PATTERNS = [
  // 希死念慮・自傷
  "死にたい", "しにたい", "死のう", "死ぬしかない", "もう死", "消えたい", "消えてしまいたい",
  "いなくなりたい", "自殺", "自死", "自傷", "リストカット", "リスカ", "手首を切", "首を吊", "首吊",
  "飛び降り", "電車に飛び込", "過剰摂取", "オーバードーズ", "ODしたい", "薬を大量",
  "生きていたくない", "生きてる意味", "消えてなくなりたい",
  // 身体的ハラスメント・性被害・暴力
  "殴られ", "蹴られ", "叩かれ", "触られ", "体を触", "胸を触", "抱きつかれ", "キスされ",
  "無理やり", "無理矢理", "性的な", "セクハラ", "レイプ", "盗撮", "つきまとわれ", "ストーカー",
  // 家庭内の安全（DV等）
  "殴られる", "暴力を振る", "家に帰るのが怖",
  // 重大コンプライアンス
  "経費を私的", "着服", "横領", "不正を指示", "粉飾", "隠蔽", "データを改ざん",
];

// 直近のユーザー発言に重篤な兆候が含まれるか
export function detectCritical(text) {
  if (!text) return false;
  const normalized = String(text);
  return CRITICAL_PATTERNS.some((kw) => normalized.includes(kw));
}
