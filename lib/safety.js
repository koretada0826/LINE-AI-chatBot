// 希死念慮・自傷・重大な暴力・不正など、即時に人へつなぐべき語
const CRITICAL_PATTERNS = [
  // 希死念慮・自傷
  "死にたい", "しにたい", "消えたい", "消えてしまいたい", "いなくなりたい",
  "自殺", "自死", "リストカット", "手首を切", "生きていたくない", "もう限界",
  // 身体的ハラスメント・暴力
  "殴られ", "蹴られ", "叩かれ", "触られ", "体を触", "胸を触", "抱きつかれ",
  "無理やり", "性的な",
  // 重大コンプライアンス
  "経費を私的", "着服", "横領", "不正を指示", "粉飾", "隠蔽",
];

// 直近のユーザー発言に重篤な兆候が含まれるか
export function detectCritical(text) {
  if (!text) return false;
  const normalized = String(text);
  return CRITICAL_PATTERNS.some((kw) => normalized.includes(kw));
}
